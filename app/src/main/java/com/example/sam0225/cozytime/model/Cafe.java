public class Cafe {
	private String id;
	private String name;
	private String city;
	private float wifi;
	private float seat;
	private float quiet;
	private float tasty;
	private float cheap;
	private float music;
	private String url;
	private String address;
	private double latitude;
	private double longitude;

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getCity() {
		return city;
	}

	public float getWifi() {
		return wifi;
	}

	public float getSeat() {
		return seat;
	}

	public float getQuiet() {
		return quiet;
	}

	public float getTasty() {
		return tasty;
	}

	public float getCheap() {
		return cheap;
	}

	public float getMusic() {
		return music;
	}

	public String getUrl() {
		return url;
	}

	public String getAddress() {
		return address;
	}

	public double getLatitude() {
		return latitude;
	}

	public double getLongitude() {
		return longitude;
	}
}
